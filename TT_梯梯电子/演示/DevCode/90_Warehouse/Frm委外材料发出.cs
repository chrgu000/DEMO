        private void GetGrid()
        {
            chkAll.Checked = false;

            int iFocRow = gridView1.FocusedRowHandle;
            string sSQL = @"
select cast(0 as bit) as 选择,a.cCode as 单据号,a.dDate as 单据日期,a.cWhCode as 仓库编码,g.cWhName as 仓库, a.cVenCode as 供应商编码,f.cVenName as 供应商,b.comcode as 委外订单号, b.cInvCode as 物料编码,e.cInvName as 物料名称,e.cInvStd as 规格型号 
	    ,b.iQuantity - isnull(d.iqty,0) as 待指定数量,b.iNum - isnull(d.inum,0) as 待指定件数
	    ,b.iQuantity as 数量,b.iNum as 件数,d.iqty as 货位数量,d.inum as 货位件数,b.iOMoDID 
       ,111111cast(b.autoid as varchar(50)) as 条形码 
       ,e.cPosition 货位编码,h.cPosName as 货位,cast(cc.iQtyCurr as decimal(16,2)) as 现存量
from @u8.rdrecord11 a inner join @u8.rdrecords11 b on a.id = b.ID 
	   inner join @u8.Warehouse c on c.cWhCode = a.cWhCode 
	   left join (select rdsid,cInvCode,sum(iQuantity) as iqty,sum(iNum) as inum from @u8.InvPosition group by rdsid,cInvCode) d on d.RdsID = b.AutoID 
	   inner join @u8.Inventory e on e.cInvCode = b.cInvCode
	   inner join @u8.Vendor f on f.cVenCode = a.cVenCode
       inner join @u8.Warehouse  g on g.cWhCode = a.cWhCode 
       left join @u8.Position h on h.cPosCode = e.cPosition 
       left join (select sum(iQuantity) as iQtyCurr,cWhCode,cInvCode  from @u8.CurrentStock group by cWhCode,cInvCode) cc on cc.cInvCode = b.cInvCode and a.cWhCode = cc.cWhcode 
where ISNULL(a.cHandler,'') = '' and ISNULL(a.cAccounter,'') = '' and ISNULL(c.bWhPos,0) = 1 and cRdCode = '211' and b.iQuantity > isnull(d.iqty,0) 
    and isnull(b.cbaccounter ,'') = ''
order by a.id,b.autoid,b.iOMoDID
";
            sSQL = sSQL.Replace("111111", "'8$200$' + ");
            dtBingGrid = clsSQLCommond.ExecQuery(sSQL);
            gridControl1.DataSource = dtBingGrid;

            gridView1.FocusedRowHandle = iFocRow;
        }

        private void gridView1_CustomDrawRowIndicator(object sender, DevExpress.XtraGrid.Views.Grid.RowIndicatorCustomDrawEventArgs e)
        {
            e.Appearance.TextOptions.HAlignment = DevExpress.Utils.HorzAlignment.Far;
            if (e.Info.IsRowIndicator)
            {
                if (e.RowHandle >= 0)
                {
                    e.Info.DisplayText = (e.RowHandle + 1).ToString();
                }
                else if (e.RowHandle < 0 && e.RowHandle > -1000)
                {
                    e.Info.Appearance.BackColor = System.Drawing.Color.AntiqueWhite;
                    e.Info.DisplayText = "G" + e.RowHandle.ToString();
                }
            }
        }

        private void chkAll_CheckedChanged(object sender, EventArgs e)
        {
            try
