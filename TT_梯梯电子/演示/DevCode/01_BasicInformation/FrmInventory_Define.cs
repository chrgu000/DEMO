            }

            aList = new System.Collections.ArrayList();
            for (int iRow = gridView1.RowCount - 1; iRow >= 0; iRow--)
            {
                if (gridView1.IsRowSelected(iRow))
                {
                    sSQL = "Delete [_Inventory_Define] where cInvCode = '" + gridView1.GetRowCellValue(iRow, gridColcInvCode).ToString().Trim() + "'";
                    
                    aList.Add(sSQL);
                }
            }

            if (aList.Count > 0)
            {
                int iCou = clsSQLCommond.ExecSqlTran(aList);
                MessageBox.Show("删除成功！\n合计执行语句:" + iCou + "条");
                GetGrid();
            }
        }

    
        /// <summary>
        /// 保存
        /// </summary>
        private void btnSave()
        {
            try 
            {
                gridView1.FocusedRowHandle -= 1;
                gridView1.FocusedRowHandle += 1;
            }
            catch { }

            string sErr = "";

            aList = new System.Collections.ArrayList();
            for (int i = 0; i < gridView1.RowCount; i++)
            {
                if (gridView1.GetRowCellDisplayText(i, gridColcInvCode).ToString().Trim() == "")
                {
                    continue;
                }

                string sInvCode = gridView1.GetRowCellValue(i,gridColcInvCode).ToString().Trim();
                string sRemark =  gridView1.GetRowCellValue(i,gridColRemark).ToString().Trim();
                int bDefine1 = ReturnObjectToInt(ReturnObjectToBool(gridView1.GetRowCellValue(i, gridColiDefine1)));

                sSQL = @"
if exists (select * from [_Inventory_Define] where cInvCode = N'{0}')
	update [_Inventory_Define] set [iDefine1] = {1},[Remark] = N'{2}'
	where cInvCode = N'{0}'
else
	insert into [_Inventory_Define](cInvCode,[iDefine1],[Remark])
	values(N'{0}',{1},N'{2}')
";
                sSQL = string.Format(sSQL, sInvCode, bDefine1, sRemark);
                aList.Add(sSQL);
            }

            if (sErr.Trim().Length > 0)
            {
                MsgBox("提示", sErr);
                return;
            }

            if (aList.Count > 0)
            {
                int iCou = clsSQLCommond.ExecSqlTran(aList);
                MessageBox.Show("保存成功！\n合计执行语句:" + iCou + "条");
                GetGrid();
            }
        }
