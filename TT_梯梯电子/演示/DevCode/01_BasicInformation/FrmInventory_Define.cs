        /// <summary>
        /// 删除
        /// </summary>
        private void btnDel()
        {
            string sErr = "";
            
            DialogResult d = MessageBox.Show("确定删除选中的 " + gridView1.SelectedRowsCount + " 行存货么？\n是：继续\n否：取消", "提示", MessageBoxButtons.YesNo, MessageBoxIcon.Asterisk);
