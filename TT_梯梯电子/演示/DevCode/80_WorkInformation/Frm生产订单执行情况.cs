        private void GetGrid()
        {
            int iFocRow = gridView1.FocusedRowHandle;
            if (dateEdit1.Text.Trim() == "" || dateEdit2.Text.Trim() == "")
            {
                MessageBox.Show("请输入日期");
                return;
            }

            string sSQL = @"
select distinct 单据号,销售订单号,销售订单行号,b.物料编码
into #a
from  dbo.生产计划  a inner join dbo.生产计划明细 b on a.单据号 = b.表头单据号
where isnull(手工计划ID,0)=0 and 2=2

select distinct b.MoCode as 生产订单号,c.SortSeq  as 行号,c.SoCode  as 销售订单号,c.SoSeq as 行, c.InvCode as 存货编码,d.cInvName as 存货名称,d.cInvStd as 规格型号
	, a.StartDate as 开工日期,a.DueDate as 完工日期,b.CreateDate as 制单日期
	,case when c.Status = 1 then '新增' when c.Status = 2 then '锁定' when c.Status = 3 then '审核'  when c.Status = 4 then '关闭' end  as 状态
	,c.Qty as 制造令数量 ,c.QualifiedInQty as 入库数量
    ,e.单据号 as 生产计划单据号
from @u8.mom_morder a inner join @u8.mom_order b on a.moid = b.moid
	inner join @u8.mom_orderdetail c on b.MoId = c.MoId 
	inner join @u8.Inventory d on c.invcode = d.cinvcode
     left join #a e on e.销售订单号 = c.SoCode and e.销售订单行号 = c.SoSeq and e.物料编码 = c.InvCode
where a.StartDate >= '111111' and a.StartDate <= '222222'
    and 1=1
order by b.MoCode,c.SortSeq
";
            sSQL = sSQL.Replace("111111", dateEdit1.DateTime.ToString("yyyy-MM-dd"));
            sSQL = sSQL.Replace("222222", dateEdit2.DateTime.ToString("yyyy-MM-dd"));
            if (lookUpEdit外销订单号码1.Text.Trim() != "")
            {
                sSQL = sSQL.Replace("1=1", " 1=1 and c.SoCode >= '" + lookUpEdit外销订单号码1.Text.Trim() + "' ");
                sSQL = sSQL.Replace("2=2", " 2=2 and 销售订单号 >= '" + lookUpEdit外销订单号码1.Text.Trim() + "' ");
            }
            if (lookUpEdit外销订单号码2.Text.Trim() != "")
            {
                sSQL = sSQL.Replace("1=1", " 1=1 and c.SoCode <= '" + lookUpEdit外销订单号码2.Text.Trim() + "' ");
                sSQL = sSQL.Replace("2=2", " 2=2 and 销售订单号 <= '" + lookUpEdit外销订单号码2.Text.Trim() + "' ");
            }
            if (lookUpEdit制造令号码1.Text.Trim() != "")
            {
                sSQL = sSQL.Replace("1=1", " 1=1 and b.MoCode >= '" + lookUpEdit制造令号码1.Text.Trim() + "' ");
            }
            if (lookUpEdit制造令号码2.Text.Trim() != "")
            {
                sSQL = sSQL.Replace("1=1", " 1=1 and b.MoCode <= '" + lookUpEdit制造令号码2.Text.Trim() + "' ");
            }

            dtBingGrid = clsSQLCommond.ExecQuery(sSQL);
            gridControl1.DataSource = dtBingGrid;

            gridView1.AddNewRow();

            gridView1.FocusedRowHandle = iFocRow;
        }

        private void gridView1_CustomDrawRowIndicator(object sender, DevExpress.XtraGrid.Views.Grid.RowIndicatorCustomDrawEventArgs e)
        {
            e.Appearance.TextOptions.HAlignment = DevExpress.Utils.HorzAlignment.Far;
            if (e.Info.IsRowIndicator)
            {
                if (e.RowHandle >= 0)
                {
                    e.Info.DisplayText = (e.RowHandle + 1).ToString();
                }
                else if (e.RowHandle < 0 && e.RowHandle > -1000)
                {
                    e.Info.Appearance.BackColor = System.Drawing.Color.AntiqueWhite;
                    e.Info.DisplayText = "G" + e.RowHandle.ToString();
                }
            }
        }

        /// <summary>
