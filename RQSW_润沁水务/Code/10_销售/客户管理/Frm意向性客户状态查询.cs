        private void GetGrid()
        {
            string sSQL = "select distinct cIntName as 客户名称,CAST(null as varchar(50)) as 业务员 " +
                        "	,CAST(null as varchar(50)) as 部门   " +
                        "	,CAST(null as varchar(50)) as 联系人 ,CAST(null as varchar(50)) as 联系电话   ,CAST(null as varchar(50)) as 地址 ,CAST(null as varchar(50)) as 状态 ,CAST(null as varchar(50)) as 报备时间   " +
                        "from CustomersIntentionality where isnull(cIntCode,'') = '' and isnull(cIntName,'') <> ''  " +
                        "order by cIntName";
            DataTable dt = clsSQLCommond.ExecQuery(sSQL);

            sSQL = "select a.*,b.PersonName,c.cDepName " +
                   "from CustomersIntentionality a  " +
                   "    left join Person b on a.cIntPerson = b.PersonCode " +
                   "    left join Department c on c.cDepCode = a.cIntDept " +
                   "where isnull(cIntCode,'') = '' and isnull(cIntName,'') <> '' " +
                   "order by cIntName,dDate desc";
            DataTable dtTemp = clsSQLCommond.ExecQuery(sSQL);

            for (int i = 0; i < dt.Rows.Count; i++)
            {
                string s客户名称 = dt.Rows[i]["客户名称"].ToString().Trim();

                DataRow[] dr = dtTemp.Select(" cIntName = '" + s客户名称 + "' ");
                if (dr.Length > 0)
                {
                    dt.Rows[i]["业务员"] = dr[0]["PersonName"];
                    dt.Rows[i]["部门"] = dr[0]["PersonName"];
                    dt.Rows[i]["联系人"] = dr[0]["s1"];
                    dt.Rows[i]["联系电话"] = dr[0]["s2"];
                    dt.Rows[i]["地址"] = dr[0]["s3"];
                    //dt.Rows[i]["状态"] = dr[0]["PersonName"];

                    if (dr[0]["cIntRegDate"].ToString().Trim() != "")
                    {
                        dt.Rows[i]["报备时间"] = Convert.ToDateTime(dr[0]["cIntRegDate"]).ToString("yyyy-MM-dd");
                    }
                }
            }

            sSQL = "select getdate()";
            DateTime dNow = Convert.ToDateTime(clsSQLCommond.ExecGetScalar(sSQL));

            int iMonth = ReturnInt(textBox1.Text.Trim());
            //DataRow[] dr列表 = dt.Select(" 报备时间 < '" + dNow.AddMonths(-1 * iMonth).ToString("yyyy-MM-dd") + "' ");

            DataView dv = dt.DefaultView;
            dv.RowFilter = " 报备时间 < '" + dNow.AddMonths(-1 * iMonth).ToString("yyyy-MM-dd") + "' ";

            gridControl1.DataSource = dv;
        }

        private void gridView1_CustomDrawRowIndicator(object sender, DevExpress.XtraGrid.Views.Grid.RowIndicatorCustomDrawEventArgs e)
        {
            e.Appearance.TextOptions.HAlignment = DevExpress.Utils.HorzAlignment.Far;
            if (e.Info.IsRowIndicator)
            {
                if (e.RowHandle >= 0)
                {
                    e.Info.DisplayText = (e.RowHandle + 1).ToString();
                }
                else if (e.RowHandle < 0 && e.RowHandle > -1000)
                {
                    e.Info.Appearance.BackColor = System.Drawing.Color.AntiqueWhite;
                    e.Info.DisplayText = "G" + e.RowHandle.ToString();
                }
            }
        }
