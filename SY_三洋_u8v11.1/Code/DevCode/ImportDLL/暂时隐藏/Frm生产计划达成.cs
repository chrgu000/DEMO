        private void gridView1_CustomDrawRowIndicator(object sender, DevExpress.XtraGrid.Views.Grid.RowIndicatorCustomDrawEventArgs e)
        {
            e.Appearance.TextOptions.HAlignment = DevExpress.Utils.HorzAlignment.Far;
            if (e.Info.IsRowIndicator)
            {
                if (e.RowHandle >= 0)
                {
                    e.Info.DisplayText = (e.RowHandle + 1).ToString();
                }
                else if (e.RowHandle < 0 && e.RowHandle > -1000)
                {
                    e.Info.Appearance.BackColor = System.Drawing.Color.AntiqueWhite;
                    e.Info.DisplayText = "G" + e.RowHandle.ToString();
                }
            }
        }

        private void GetGrid()
        {
            for (int i = gridView1.Columns.Count - 1; i >= 0; i--)
            {
                string sColName = gridView1.Columns[i].Name.Trim();
                if (sColName.Length > 11 && sColName.Substring(0, 11) == "gridColtemp")
                {
                    gridView1.Columns.RemoveAt(i);
                }
            }

            for (int i = gridView1.Bands.Count - 1; i >= 0; i--)
            {
                string sColName = gridView1.Bands[i].Name.Trim();
                if (sColName.Length > 12 && sColName.Substring(0, 12) == "gridBandtemp")
                {
                    gridView1.Bands.RemoveAt(i);
                }
            }

            string sWhere = "1=1";
            if (lookUpEdit生产订单号1.Text.Trim() != "")
            {
                sWhere = sWhere + " and a.MOCode >= '" + lookUpEdit生产订单号1.Text.Trim() + "'";
            }
            if (lookUpEdit生产订单号2.Text.Trim() != "")
            {
                sWhere = sWhere + " and a.MOCode <= '" + lookUpEdit生产订单号2.Text.Trim() + "'";
            }



            DataTable dt = DAL.Get生产达成日期范围(sWhere, dateEdit1.DateTime, dateEdit2.DateTime);
            if (dt == null || dt.Rows.Count == 0 || dt.Rows[0][0].ToString().Trim() == "")
                throw new Exception("获得日期范围失败");

            DateTime d1 = BaseFunction.BaseFunction.ReturnDate(dt.Rows[0][0]);
            DateTime d2 = BaseFunction.BaseFunction.ReturnDate(dt.Rows[0][1]);

            DateTime d = d1;
            while (d <= d2)
            {
                AddCol(d);
                d = d.AddDays(1);
            }

            gridControl1.DataSource = DAL.Get生产达成(sWhere, d1, d2);

        }

